package beans;

/*전공 과목 빈*/
public class MajorBean {

	private int mj_key;
	private String mj_title;
	private String mj_subject;
	
	public int getMj_key() {
		return mj_key;
	}
	public void setMj_key(int mj_key) {
		this.mj_key = mj_key;
	}
	public String getMj_title() {
		return mj_title;
	}
	public void setMj_title(String mj_title) {
		this.mj_title = mj_title;
	}
	public String getMj_subject() {
		return mj_subject;
	}
	public void setMj_subject(String mj_subject) {
		this.mj_subject = mj_subject;
	}
	
}
